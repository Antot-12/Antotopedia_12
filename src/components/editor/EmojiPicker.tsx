type Props = {
  onSelect: (emoji: string) => void;
  onClose: () => void;
};

export default function EmojiPicker({ onSelect, onClose }: Props) {
  const [search, setSearch] = useState("");
  const [activeCategory, setActiveCategory] = useState<keyof typeof EMOJI_CATEGORIES>("smileys");

  const filteredEmojis = search
    ? Object.values(EMOJI_CATEGORIES)
        .flatMap((cat) => cat.emojis)
        .filter((emoji) => emoji.includes(search))
    : EMOJI_CATEGORIES[activeCategory].emojis;

  return (
    <div className="fixed inset-0 bg-black/80 backdrop-blur-sm z-50 flex items-start justify-center pt-10">
      <div className="card bg-black/95 border-white/20 p-4 w-full max-w-lg">
        {/* Header */}
        <div className="flex items-center justify-between mb-3">
          <h3 className="text-lg font-semibold">Emoji Picker</h3>
          <button
            onClick={onClose}
            className="text-white/50 hover:text-white transition text-xl"
          >
            ✕
          </button>
        </div>

        {/* Search */}
        <input
          type="text"
          value={search}
          onChange={(e) => setSearch(e.target.value)}
          placeholder="Search emojis..."
          className="input mb-3"
          autoFocus
        />

        {/* Categories */}
        {!search && (
          <div className="flex gap-1 mb-3 overflow-x-auto pb-2 custom-scrollbar">
            {Object.entries(EMOJI_CATEGORIES).map(([key, cat]) => (
              <button
                key={key}
                onClick={() => setActiveCategory(key as keyof typeof EMOJI_CATEGORIES)}
                className={`px-3 py-2 rounded-lg transition flex-shrink-0 ${
                  activeCategory === key
                    ? "bg-accent text-black"
                    : "bg-white/5 hover:bg-white/10"
                }`}
                title={cat.name}
              >
                <span className="text-xl">{cat.icon}</span>
              </button>
            ))}
          </div>
        )}

        {/* Emoji Grid */}
        <div className="h-72 overflow-y-auto custom-scrollbar bg-white/5 rounded-lg p-3">
          <div className="grid grid-cols-8 gap-2">
            {filteredEmojis.map((emoji, index) => (
              <button
                key={`${emoji}-${index}`}
                onClick={() => {
                  onSelect(emoji);
                  onClose();
                }}
                className="text-2xl hover:bg-white/10 rounded p-2 transition"
                title={emoji}
              >
                {emoji}
              </button>
            ))}
          </div>

          {filteredEmojis.length === 0 && (
            <div className="text-center text-white/50 py-8">
              No emojis found
            </div>
          )}
        </div>

        {/* Footer */}
        <div className="text-[10px] text-white/40 mt-3 text-center">
          Click an emoji to insert it at cursor position
        </div>
      </div>
    </div>
  );
}
